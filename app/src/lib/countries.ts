export const COUNTRY_NAMES = Object.keys(COUNTRY_COORDS).sort((a, b) => a.localeCompare(b))

export function resolveCountry(input: string | undefined | null): [number, number] | null {
  if (!input) return null
  const trimmed = input.trim()
  if (!trimmed) return null
  if (COUNTRY_COORDS[trimmed]) return COUNTRY_COORDS[trimmed]
  const lower = trimmed.toLowerCase()
  if (ALIASES[lower]) return COUNTRY_COORDS[ALIASES[lower]]
  const match = COUNTRY_NAMES.find((name) => name.toLowerCase() === lower)
  return match ? COUNTRY_COORDS[match] : null
}
